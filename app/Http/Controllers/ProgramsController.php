    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $program = new Program;
        $program->departmentid = $request->departmentid;
        $program->programname = $request->programname;
        $program->save();
        return redirect('all-programs');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show()
    {
        //
        $programs = Program::select('departments.dep_name','programs.programname')
						->join('departments','programs.departmentid','departments.id')
						->get();
		return view('dashboard1.all-programs',compact('programs'));
    }

