     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
        $program = Program::find($id);
        $program->delete();
        return redirect('all-programs')->with('message1','Program deleted from the database');
    }
